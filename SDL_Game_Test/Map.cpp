Map::Map() 
{
	dirt = TextureManage::LoadTexture("resource/dirt.png");
	grass = TextureManage::LoadTexture("resource/grass.png");
	water = TextureManage::LoadTexture("resource/water.png");

	mapLoad(testMap);

	dst.h = 20;
	dst.w = 20;
	dst.x = dst.y = 0;
};

Map::~Map() 
{
	SDL_DestroyTexture(grass);
	SDL_DestroyTexture(water);
	SDL_DestroyTexture(dirt);
};

void Map::mapLoad(int a[24][32]) 
{
	for (int i = 0; i < 24; i++)
	{
		for (int j = 0; j < 32; j++)
		{
			map[i][j] = a[i][j];
		}
	}
};

void Map::drawMap() 
{
	int type = 0;
	for (int i = 0; i < 24; i++)
	{
		for (int j = 0; j < 32; j++)
		{
			type = map[i][j];

			dst.x = j * 20;
			dst.y = i * 20;
			switch (type)
			{
			case 0:
			{
				TextureManage::Draw(dirt, src, dst);
				break;
			}
			case 1:
			{
				TextureManage::Draw(water, src, dst);
				break;
			}
			case 2:
			{
				TextureManage::Draw(grass, src, dst);
				break;
			}
			}

		}
	}
};